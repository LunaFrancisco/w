import { auth } from '@/lib/auth'
import { redirect } from 'next/navigation'
import AdminNavigation from '@/components/AdminNavigation'

export default async function AdminLayout({
  children,
}: {
  children: React.ReactNode
}) {
  const session = await auth()

  if (!session) {
    redirect('/auth/signin')
  }

  if (session.user.role !== 'ADMIN') {
    redirect('/auth/unauthorized')
  }

  return (
    <div className="min-h-screen bg-gray-50">
      <AdminNavigation />
      <div className="flex">
        <aside className="hidden lg:block w-64 bg-white shadow-sm">
          <nav className="h-full pt-4">
            <div className="px-4 pb-4">
              <h2 className="text-lg font-semibold text-gray-900">Panel Admin</h2>
            </div>
            <div className="px-3">
              <AdminSidebar />
            </div>
          </nav>
        </aside>
        <main className="flex-1 lg:pl-0">
          <div className="px-4 sm:px-6 lg:px-8 py-8">
            {children}
          </div>
        </main>
      </div>
    </div>
  )
}

function AdminSidebar() {
  const navigation = [
    { name: 'Dashboard', href: '/admin/dashboard', icon: ChartBarIcon },
    { name: 'Usuarios', href: '/admin/usuarios', icon: UsersIcon },
    { name: 'Productos', href: '/admin/productos', icon: CubeIcon },
    { name: 'Pedidos', href: '/admin/pedidos', icon: ShoppingBagIcon },
    { name: 'Solicitudes', href: '/admin/solicitudes', icon: DocumentCheckIcon },
    { name: 'Reportes', href: '/admin/reportes', icon: DocumentChartBarIcon },
  ]

  return (
    <ul role="list" className="space-y-1">
      {navigation.map((item) => (
        <li key={item.name}>
          <a
            href={item.href}
            className="group flex gap-x-3 rounded-md p-2 text-sm leading-6 font-semibold text-gray-700 hover:text-orange-600 hover:bg-orange-50"
          >
            <item.icon className="h-6 w-6 shrink-0" aria-hidden="true" />
            {item.name}
          </a>
        </li>
      ))}
    </ul>
  )
}

